package search;

/**
 * Класс Task_2 реализует алгоритмы поиска в отсортированных массивах:
 * - Бинарный поиск (Binary Search)
 * - Интерполяционный поиск (Interpolation Search)
 *
 * Оба алгоритма предназначены для работы с отсортированными по возрастанию
 * массивами целых чисел.
 *
 * Временная сложность:
 * - Бинарный поиск: O(log n) в среднем и худшем случае.
 * - Интерполяционный поиск: O(log log n) в среднем для равномерно распределённых данных,
 *   но в худшем случае может быть O(n).
 *
 * Пространственная сложность: O(1) (используется только несколько переменных).
 *
 * @author Томских Артём ИВТ-23
 */
public class Task_2 {

    /**
     * Выполняет бинарный поиск элемента в отсортированном массиве.
     *
     * Алгоритм:
     * 1. Устанавливаем левую (left) и правую (right) границы поиска.
     * 2. Пока left <= right:
     *    a. Вычисляем средний индекс middle = left + (right - left) / 2.
     *    b. Если array[middle] равен искомому, возвращаем middle.
     *    c. Если array[middle] меньше искомого, сдвигаем left = middle + 1.
     *    d. Иначе сдвигаем right = middle - 1.
     * 3. Если элемент не найден, возвращаем -1.
     *
     * @param sortedArray отсортированный по возрастанию массив целых чисел
     * @param element     значение, которое необходимо найти
     * @return индекс элемента, если он найден; иначе -1
     *
     * @throws NullPointerException если массив равен null
     *
     * @see #interpolationSearch(int[], int)
     */
    public static int binaryOfIndex(int[] sortedArray, int element) {
        int left = 0;                       // левая граница поиска
        int right = sortedArray.length - 1; // правая граница поиска

        while (left <= right) {
            // Вычисляем середину, избегая переполнения через (left + right) / 2
            int middle = left + (right - left) / 2;
            int current = sortedArray[middle];

            if (current == element) {
                return middle;               // элемент найден
            } else if (current < element) {
                left = middle + 1;            // искомое число больше, сужаем диапазон справа
            } else { // current > element
                right = middle - 1;           // искомое число меньше, сужаем диапазон слева
            }
        }

        return -1; // элемент отсутствует
    }

    /**
     * Выполняет интерполяционный поиск элемента в отсортированном массиве.
     *
     * Интерполяционный поиск улучшает бинарный, используя информацию о значении
     * искомого элемента для оценки его позиции. Предполагается, что данные
     * распределены равномерно. Формула оценки позиции:
     * pos = low + ((key - array[low]) * (high - low) / (array[high] - array[low]))
     *
     * Алгоритм:
     * 1. Если массив пуст, вернуть -1.
     * 2. Проверить крайние элементы (low и high) на равенство ключу.
     * 3. Пока key находится в пределах [array[low], array[high]] и low <= high:
     *    a. Если все элементы в диапазоне равны (array[high] == array[low]),
     *       то проверить array[low] и вернуть результат.
     *    b. Вычислить предполагаемую позицию pos по формуле интерполяции.
     *    c. Ограничить pos значениями [low, high] для безопасности.
     *    d. Сравнить array[pos] с key:
     *       - если равно, вернуть pos;
     *       - если меньше, сдвинуть low = pos + 1;
     *       - если больше, сдвинуть high = pos - 1.
     * 4. Если элемент не найден, вернуть -1.
     *
     * Особенности реализации:
     * - Используется long в числителе для предотвращения переполнения int.
     * - Проверка на одинаковые элементы (array[high] == array[low]) предотвращает деление на ноль.
     * - Принудительное ограничение pos гарантирует, что индекс не выйдет за границы массива.
     *
     * @param array отсортированный по возрастанию массив целых чисел
     * @param key   значение, которое необходимо найти
     *
     * @return индекс элемента, если он найден; иначе -1
     *
     * @throws NullPointerException если массив равен null
     */
    public static int interpolationSearch(int[] array, int key) {
        int low = 0;
        int high = array.length - 1;

        // Пустой массив
        if (array.length == 0) return -1;

        // Проверка крайних элементов для ускорения
        if (array[low] == key) return low;
        if (array[high] == key) return high;

        while (low <= high && key >= array[low] && key <= array[high]) {
            // Если все элементы в диапазоне одинаковы, деление на ноль невозможно,
            // но формула интерполяции не работает. Просто проверяем текущий элемент.
            if (array[high] == array[low]) {
                // Все элементы равны; проверяем, равен ли ключ этому значению
                return (array[low] == key) ? low : -1;
            }

            // Оценка позиции с использованием интерполяции.
            // Используем long для числителя, чтобы избежать переполнения при умножении.
            long numerator = (long) (key - array[low]) * (high - low);
            int pos = low + (int) (numerator / (array[high] - array[low]));

            // Защита от выхода индекса за допустимые пределы из-за погрешностей округления
            pos = Math.max(low, Math.min(high, pos));

            if (array[pos] == key) {
                return pos;
            } else if (array[pos] < key) {
                low = pos + 1;       // искомое число больше, двигаем нижнюю границу
            } else {
                high = pos - 1;      // искомое число меньше, двигаем верхнюю границу
            }
        }

        return -1; // элемент не найден
    }
}