package search;

import java.io.FileWriter;
import java.io.IOException;

/**
 * Класс ClosestElementFinder предоставляет функциональность для поиска элемента в массиве,
 * наиболее близкого к заданному числу.
 *
 * Основные возможности:
 * 1. Поиск ближайшего элемента в массиве целых чисел
 * 2. Сохранение результатов поиска в файл
 * 3. Форматирование массива для вывода
 *
 * Алгоритм поиска использует полный перебор (линейный поиск) с вычислением
 * абсолютной разницы между каждым элементом и искомым числом.
 *
 * Временная сложность: O(n), где n - размер массива
 * Пространственная сложность: O(1)
 *
 * @author Томских Артём ИВТ-23
 */
public class ClosestElementFinder {

    /**
     * Находит элемент в массиве, наиболее близкий к заданному числу x.
     *
     * Алгоритм работы:
     * 1. Проверяет массив на пустоту (выбрасывает исключение если массив пуст)
     * 2. Принимает первый элемент как начальное приближение ближайшего элемента
     * 3. Вычисляет абсолютную разницу между первым элементом и x как минимальную
     * 4. Последовательно проходит по всем остальным элементам массива
     * 5. Для каждого элемента вычисляет абсолютную разницу с x
     * 6. Если текущая разница меньше сохраненной минимальной, обновляет ближайший элемент и минимум
     * 7. Возвращает найденный ближайший элемент
     *
     * Особенности:
     * - При равенстве разниц (например, x = 5, элементы 4 и 6) возвращается первый встреченный
     * - Для отрицательных чисел также корректно работает (используется Math.abs)
     * - Если в массиве есть элемент, точно равный x, он будет возвращен (разница = 0)
     *
     * @param arr входной массив целых чисел (не должен быть null или пустым)
     * @param x   число, к которому ищем ближайший элемент
     *
     * @return элемент массива, наиболее близкий к x
     *
     * @throws IllegalArgumentException если массив пустой или равен null
     */
    public static int findClosestElement(int[] arr, int x) {
        // Проверка корректности входных данных
        if (arr == null || arr.length == 0) {
            throw new IllegalArgumentException("Массив не может быть пустым или null");
        }

        // Инициализация первым элементом
        int closest = arr[0];
        int minDiff = Math.abs(arr[0] - x);

        // Линейный проход по остальным элементам массива
        for (int i = 1; i < arr.length; i++) {
            int currentDiff = Math.abs(arr[i] - x);

            // Если нашли элемент ближе к x, обновляем результат
            if (currentDiff < minDiff) {
                closest = arr[i];
                minDiff = currentDiff;
            }
        }

        return closest;
    }

    /**
     * Записывает результаты поиска в текстовый файл.
     *
     * Формат выходного файла:
     * - Первая строка: исходный массив в формате [эл1, эл2, ...]
     * - Вторая строка: искомое число x
     * - Третья строка: найденный ближайший элемент
     *
     * Особенности реализации:
     * - Использует try-with-resources для автоматического закрытия FileWriter
     * - В случае ошибки записи выводит сообщение в System.err
     * - Файл создается или перезаписывается при каждом вызове
     *
     * @param filename имя файла для записи (может содержать путь)
     * @param arr      исходный массив, в котором производился поиск
     * @param x        искомое число
     * @param result   найденный ближайший элемент
     */
    public static void writeResultToFile(String filename, int[] arr, int x, int result) {
        // Используем try-with-resources для гарантированного закрытия ресурса
        try (FileWriter writer = new FileWriter(filename)) {
            writer.write("Массив: " + arrayToString(arr) + "\n");
            writer.write("Число x: " + x + "\n");
            writer.write("Ближайший элемент: " + result + "\n");

            System.out.println("Результат успешно записан в файл: " + filename);
        } catch (IOException e) {
            // Обработка ошибок ввода-вывода без прерывания программы
            System.err.println("Ошибка при записи в файл '" + filename + "': " + e.getMessage());
        }
    }

    /**
     * Преобразует массив целых чисел в строковое представление.
     *
     * Формат вывода: [элемент1, элемент2, ..., элементN]
     *
     * Алгоритм:
     * 1. Создает StringBuilder с открывающей квадратной скобкой
     * 2. Проходит по всем элементам массива, добавляя каждый элемент
     * 3. Добавляет запятую и пробел после каждого элемента, кроме последнего
     * 4. Добавляет закрывающую квадратную скобку
     *
     * @param arr массив целых чисел для преобразования
     *
     * @return строковое представление массива в формате [a, b, c, ...]
     */
    public static String arrayToString(int[] arr) {
        // Обработка null и пустого массива для устойчивости
        if (arr == null) {
            return "null";
        }
        if (arr.length == 0) {
            return "[]";
        }

        StringBuilder sb = new StringBuilder("[");
        for (int i = 0; i < arr.length; i++) {
            sb.append(arr[i]);
            // Добавляем разделитель только между элементами, не после последнего
            if (i < arr.length - 1) {
                sb.append(", ");
            }
        }
        sb.append("]");
        return sb.toString();
    }

    /**
     * Демонстрационный метод для тестирования функциональности класса.
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        // Пример использования класса
        System.out.println("=== Демонстрация работы ClosestElementFinder ===\n");

        // Тестовый массив
        int[] testArray = {10, 22, 35, 47, 53, 68, 71, 89, 94, 100};
        int x = 50;

        System.out.println("Исходный массив: " + arrayToString(testArray));
        System.out.println("Искомое число x: " + x);

        // Поиск ближайшего элемента
        int closest = findClosestElement(testArray, x);
        System.out.println("Ближайший элемент: " + closest);

        // Сохранение результата в файл
        writeResultToFile("closest_result.txt", testArray, x, closest);
        System.out.println("\nРезультат также сохранен в файл 'closest_result.txt'");
    }
}