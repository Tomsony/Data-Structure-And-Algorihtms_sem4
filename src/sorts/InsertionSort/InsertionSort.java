package sorts.InsertionSort;

/**
 * Класс InsertionSort реализует алгоритм сортировки вставками.
 *
 * Принцип работы:
 * 1. Массив разделяется на отсортированную и неотсортированную части
 * 2. На каждом шаге берется первый элемент из неотсортированной части
 * 3. Этот элемент вставляется в правильную позицию в отсортированной части
 * 4. Элементы больше текущего сдвигаются вправо
 *
 * Сложность алгоритма:
 * - Временная сложность:
 *   * Худший случай: O(n²) - массив отсортирован в обратном порядке
 *   * Средний случай: O(n²) - требуется примерно n²/4 сравнений
 *   * Лучший случай: O(n) - массив уже отсортирован
 * - Пространственная сложность: O(1) - сортировка выполняется на месте
 *
 * Количество операций для массива размера n:
 * - Сравнений в худшем случае: n(n-1)/2 ≈ n²/2
 * - Сравнений в лучшем случае: n-1
 * - Перемещений в худшем случае: n(n-1)/2 + (n-1) ≈ n²/2
 *
 * Преимущества:
 * - Простота реализации
 * - Эффективен для частично отсортированных массивов
 * - Стабильная сортировка (сохраняет порядок равных элементов)
 * - Работает "на месте", не требует дополнительной памяти
 *
 * Недостатки:
 * - Неэффективен для больших массивов
 * - Квадратичная сложность в среднем случае
 *
 * @author Томских Артём ИВТ-23
 */
public class InsertionSort {

    // Счетчики для анализа производительности
    private static int comparisonCount = 0;  // количество сравнений
    private static int swapCount = 0;        // количество перемещений (сдвигов + вставок)

    /**
     * Сбрасывает счетчики операций.
     * Используется перед началом нового измерения.
     */
    public static void resetCounters() {
        comparisonCount = 0;
        swapCount = 0;
    }

    /**
     * Возвращает количество выполненных сравнений.
     *
     * @return количество сравнений
     */
    public static int getComparisonCount() {
        return comparisonCount;
    }

    /**
     * Возвращает количество выполненных перемещений.
     *
     * @return количество перемещений (сдвигов элементов и вставок)
     */
    public static int getSwapCount() {
        return swapCount;
    }

    /**
     * Сортирует массив целых чисел методом вставок.
     *
     * Алгоритм работы:
     * 1. Начинаем со второго элемента (индекс 1), считая первый элемент отсортированной частью
     * 2. Сохраняем текущий элемент в переменную key
     * 3. Сравниваем key с элементами отсортированной части (справа налево)
     * 4. Если элемент отсортированной части больше key, сдвигаем его вправо
     * 5. Повторяем шаги 3-4 пока не найдем правильную позицию для key
     * 6. Вставляем key в найденную позицию
     * 7. Переходим к следующему элементу
     *
     * @param arr входной массив целых чисел (может быть null)
     * @return отсортированный массив (тот же объект, что и входной, или null)
     */
    public static int[] insertionSort(int[] arr) {
        // Проверка на null или пустой массив
        if (arr == null || arr.length <= 1) {
            return arr; // Массив уже отсортирован (или не требует сортировки)
        }

        // Внешний цикл: проходим по всем элементам, начиная со второго
        for (int i = 1; i < arr.length; i++) {
            int key = arr[i]; // Текущий элемент, который нужно вставить в отсортированную часть
            int j = i - 1;    // Индекс последнего элемента отсортированной части

            // Внутренний цикл: сдвигаем элементы вправо, пока не найдем место для key
            // Условие j >= 0 защищает от выхода за границы массива
            // arr[j] > key - пока текущий элемент больше key, сдвигаем
            while (j >= 0 && arr[j] > key) {
                comparisonCount++; // Увеличиваем счетчик сравнений

                // Сдвигаем элемент вправо
                arr[j + 1] = arr[j];
                swapCount++; // Увеличиваем счетчик перемещений

                j--; // Переходим к предыдущему элементу
            }

            // Добавляем последнее сравнение, которое завершило цикл
            // (когда j < 0 или arr[j] <= key)
            if (j >= 0) {
                comparisonCount++; // Сравнение arr[j] <= key
            }

            // Вставляем key в правильную позицию
            arr[j + 1] = key;
            swapCount++; // Увеличиваем счетчик перемещений (вставка)
        }

        return arr;
    }

    /**
     * Возвращает строку с анализом производительности для массива заданного размера.
     *
     * @param size размер массива
     * @return строка с анализом
     */
    public static String getComplexityAnalysis(int size) {
        StringBuilder sb = new StringBuilder();
        sb.append("Анализ сортировки вставками для n = ").append(size).append("\n");
        sb.append("========================================\n");
        sb.append("Лучший случай (уже отсортирован):\n");
        sb.append("  - Сравнений: ").append(size - 1).append("\n");
        sb.append("  - Перемещений: ").append(2 * (size - 1)).append("\n");
        sb.append("  - Сложность: O(n)\n\n");

        sb.append("Худший случай (обратный порядок):\n");
        sb.append("  - Сравнений: ").append(size * (size - 1) / 2).append("\n");
        sb.append("  - Перемещений: ").append(size * (size - 1) / 2 + (size - 1)).append("\n");
        sb.append("  - Сложность: O(n²)\n\n");

        sb.append("Средний случай:\n");
        sb.append("  - Сравнений: ~").append(size * (size - 1) / 4).append("\n");
        sb.append("  - Перемещений: ~").append(size * (size - 1) / 4 + (size - 1)).append("\n");
        sb.append("  - Сложность: O(n²)\n");

        return sb.toString();
    }

    /**
     * Вспомогательный метод для преобразования массива в строку.
     *
     * @param arr массив для преобразования
     * @return строковое представление массива
     */
    private static String arrayToString(int[] arr) {
        if (arr == null) return "null";
        if (arr.length == 0) return "[]";

        StringBuilder sb = new StringBuilder("[");
        for (int i = 0; i < arr.length; i++) {
            sb.append(arr[i]);
            if (i < arr.length - 1) {
                sb.append(", ");
            }
        }
        sb.append("]");
        return sb.toString();
    }
}