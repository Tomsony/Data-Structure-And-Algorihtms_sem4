/**
 * Класс RadixSort реализует алгоритм поразрядной сортировки для целых чисел по возрастанию.
 *
 * Принцип работы:
 * 1. Сортировка выполняется поразрядно, начиная с младших разрядов (единицы, десятки и т.д.)
 * 2. Для каждого разряда используется устойчивая сортировка подсчетом (Counting Sort)
 * 3. После обработки всех разрядов массив оказывается полностью отсортированным
 *
 * Временная сложность: O(n * k), где:
 *   n - количество элементов в массиве
 *   k - количество разрядов в максимальном числе (для чисел до 999999, k = 6)
 *
 * Пространственная сложность: O(n + k) - требуется дополнительная память
 *
 * Преимущества:
 * - Линейная сложность для фиксированного размера чисел
 * - Стабильная сортировка
 * - Хорошо параллелится
 *
 * Недостатки:
 * - Требует дополнительной памяти
 * - Неэффективна для чисел с большим количеством разрядов
 * - Работает только с целыми числами в данной реализации
 *
 * @author Томских Артём ИВТ-23
 */
public class RadixSort {

    /**
     * Основной метод поразрядной сортировки.
     *
     * Алгоритм работы:
     * 1. Проверяет массив на null или пустоту
     * 2. Находит максимальное число для определения количества разрядов
     * 3. Для каждого разряда (единицы, десятки, сотни...):
     *    a. Вызывает countingSortByDigit для сортировки по текущему разряду
     *    b. Обновляет массив результатом сортировки
     * 4. Возвращает отсортированный массив
     *
     * Визуализация для массива [170, 45, 75, 90, 802, 24, 2, 66]:
     *
     * Шаг 1 (разряд единиц, exp=1):
     *   [170, 90, 802, 2, 24, 45, 75, 66] - сортировка по последней цифре
     *
     * Шаг 2 (разряд десятков, exp=10):
     *   [802, 2, 24, 45, 66, 170, 75, 90] - сортировка по предпоследней цифре
     *
     * Шаг 3 (разряд сотен, exp=100):
     *   [2, 24, 45, 66, 75, 90, 170, 802] - сортировка по первой цифре
     *
     * @param arr массив целых чисел для сортировки
     * @return отсортированный по возрастанию массив
     *
     * @throws NullPointerException если массив равен null
     */
    public static int[] radixSort(int[] arr) {
        // Проверка на null или пустой массив
        if (arr == null) {
            throw new NullPointerException("Массив не может быть null");
        }
        if (arr.length == 0) {
            return arr;  // Пустой массив возвращаем без изменений
        }

        // Находим максимальное число для определения количества разрядов
        // Используем stream API для нахождения максимума
        int max = Arrays.stream(arr).max().getAsInt();

        // Применяем сортировку подсчетом для каждого разряда
        // exp - текущий разряд: 1 (единицы), 10 (десятки), 100 (сотни) и т.д.
        for (int exp = 1; max / exp > 0; exp *= 10) {
            // Обновляем массив после сортировки по текущему разряду
            arr = countingSortByDigit(arr, exp);
        }

        return arr;
    }

    /**
     * Вспомогательная функция для сортировки подсчетом по определенному разряду.
     * Реализует устойчивую сортировку по указанной цифре числа.
     *
     * Алгоритм работы:
     * 1. Создает массив счетчиков count[10] для цифр 0-9
     * 2. Подсчитывает количество каждой цифры в текущем разряде
     * 3. Преобразует счетчики в кумулятивные (накопленные) суммы
     * 4. Проходит по массиву справа налево, размещая элементы в выходном массиве
     * 5. Возвращает новый отсортированный по данному разряду массив
     *
     * Детальный разбор для массива [170, 45, 75, 90] и exp=1 (единицы):
     *
     * Шаг 1: Подсчет цифр в разряде единиц
     *   Число: 170 -> цифра 0, count[0]++
     *   Число: 45  -> цифра 5, count[5]++
     *   Число: 75  -> цифра 5, count[5]++
     *   Число: 90  -> цифра 0, count[0]++
     *   Результат: count = [2, 0, 0, 0, 0, 2, 0, 0, 0, 0]
     *
     * Шаг 2: Кумулятивные суммы
     *   count[0] = 2
     *   count[1] = 2
     *   ...
     *   count[5] = 2 + 2 = 4
     *   Результат: count = [2, 2, 2, 2, 2, 4, 4, 4, 4, 4]
     *
     * Шаг 3: Проход справа налево
     *   i=3: arr[3]=90, цифра=0, позиция = count[0]-1 = 1
     *         output[1] = 90, count[0] = 1
     *   i=2: arr[2]=75, цифра=5, позиция = count[5]-1 = 3
     *         output[3] = 75, count[5] = 3
     *   i=1: arr[1]=45, цифра=5, позиция = count[5]-1 = 2
     *         output[2] = 45, count[5] = 2
     *   i=0: arr[0]=170, цифра=0, позиция = count[0]-1 = 0
     *         output[0] = 170, count[0] = 0
     *
     * Результат: output = [170, 90, 45, 75] - отсортировано по единицам
     *
     * @param arr массив для сортировки
     * @param exp текущий разряд (1 для единиц, 10 для десятков, 100 для сотен)
     * @return новый массив, отсортированный по указанному разряду
     */
    private static int[] countingSortByDigit(int[] arr, int exp) {
        int n = arr.length;
        int[] output = new int[n]; // Выходной массив для результата сортировки
        int[] count = new int[10]; // Счетчики для каждой цифры (0-9)

        // Инициализируем счетчики нулями
        Arrays.fill(count, 0);

        // === ШАГ 1: Подсчет количества каждой цифры ===
        // Проходим по всему массиву и считаем, сколько раз встречается каждая цифра
        for (int i = 0; i < n; i++) {
            // (arr[i] / exp) - отбрасываем младшие разряды
            // % 10 - получаем текущую цифру
            int digit = (arr[i] / exp) % 10;
            count[digit]++; // Увеличиваем счетчик для этой цифры
        }

        // === ШАГ 2: Преобразование в кумулятивные суммы ===
        // Теперь count[i] содержит количество элементов с цифрой <= i
        // Это нужно для определения позиции элементов в выходном массиве
        for (int i = 1; i < 10; i++) {
            count[i] += count[i - 1];
        }

        // === ШАГ 3: Построение выходного массива (справа налево для устойчивости) ===
        // Проходим справа налево, чтобы сохранить устойчивость сортировки
        // (элементы с одинаковыми цифрами сохраняют исходный порядок)
        for (int i = n - 1; i >= 0; i--) {
            int digit = (arr[i] / exp) % 10; // Получаем цифру текущего элемента

            // count[digit] содержит количество элементов с цифрой <= digit
            // Уменьшаем на 1, чтобы получить индекс в 0-индексации
            int position = count[digit] - 1;

            // Размещаем элемент в выходном массиве
            output[position] = arr[i];

            // Уменьшаем счетчик для этой цифры
            count[digit]--;
        }

        // Возвращаем новый отсортированный по данному разряду массив
        return output;
    }
}