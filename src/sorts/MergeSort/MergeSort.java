package sorts.MergeSort;

/**
 * Класс MergeSort реализует алгоритм сортировки слиянием.
 *
 * Принцип работы:
 * 1. Массив рекурсивно разделяется на две половины до тех пор,
 *    пока не останутся массивы из одного элемента
 * 2. Затем происходит слияние отсортированных половин в один отсортированный массив
 *
 * Преимущества алгоритма:
 * - Стабильная сортировка (сохраняет порядок равных элементов)
 * - Гарантированная сложность O(n log n) для всех случаев
 * - Хорошо работает для связных списков
 *
 * Сложность алгоритма:
 * - Временная сложность: O(n log n) для лучшего, среднего и худшего случая
 * - Пространственная сложность: O(n) - требует дополнительной памяти
 *
 * @author Томских Артём ИВТ-23
 */
public class MergeSort {

    /**
     * Выполняет слияние двух отсортированных массивов в один отсортированный массив.
     *
     * Алгоритм работы:
     * 1. Создается результирующий массив размером суммарной длины двух входных массивов
     * 2. Используются три указателя:
     *    - l (left) для индексации левого массива
     *    - r (right) для индексации правого массива
     *    - R (result) для индексации результирующего массива
     * 3. Пока оба массива не исчерпаны:
     *    - Сравниваются текущие элементы левого и правого массивов
     *    - Меньший элемент копируется в результат, соответствующий указатель смещается
     * 4. Если один из массивов закончился раньше, оставшиеся элементы второго массива
     *    дописываются в результат (они уже гарантированно больше всех записанных)
     *
     * Визуализация процесса для массивов [1, 3, 5] и [2, 4, 6]:
     *
     * Шаг 1: left[0]=1 <= right[0]=2? Да → result[0]=1
     *        l=1, r=0, R=1, result=[1, 0, 0, 0, 0, 0]
     *
     * Шаг 2: left[1]=3 <= right[0]=2? Нет → result[1]=2
     *        l=1, r=1, R=2, result=[1, 2, 0, 0, 0, 0]
     *
     * Шаг 3: left[1]=3 <= right[1]=4? Да → result[2]=3
     *        l=2, r=1, R=3, result=[1, 2, 3, 0, 0, 0]
     *
     * Шаг 4: left[2]=5 <= right[1]=4? Нет → result[3]=4
     *        l=2, r=2, R=4, result=[1, 2, 3, 4, 0, 0]
     *
     * Шаг 5: left[2]=5 <= right[2]=6? Да → result[4]=5
     *        l=3, r=2, R=5, result=[1, 2, 3, 4, 5, 0]
     *
     * Шаг 6: Левый массив закончился (l=3), дописываем оставшиеся элементы правого:
     *        result[5]=6 → result=[1, 2, 3, 4, 5, 6]
     *
     * @param left первый отсортированный массив
     * @param right второй отсортированный массив
     * @return новый массив, содержащий все элементы left и right в отсортированном порядке
     *
     * @throws NullPointerException если один из массивов равен null
     */
    public static int[] mergeSort(int[] left, int[] right) {
        // Проверка на null
        if (left == null || right == null) {
            throw new NullPointerException("Входные массивы не могут быть null");
        }

        // Создаем результирующий массив, размер которого равен сумме размеров входных массивов
        int[] result = new int[left.length + right.length];

        // Индексы для прохода по массивам:
        int l = 0; // указатель для левого массива
        int r = 0; // указатель для правого массива
        int R = 0; // указатель для результирующего массива

        // === ОСНОВНОЙ ЦИКЛ СЛИЯНИЯ ===
        // Продолжаем пока не дойдем до конца хотя бы одного из массивов
        while (l < left.length && r < right.length) {
            // Сравниваем текущие элементы левого и правого массивов
            if (left[l] <= right[r]) {
                // Элемент левого массива меньше или равен - берем его
                result[R] = left[l];
                l++; // Перемещаем указатель левого массива
            } else {
                // Элемент правого массива меньше - берем его
                result[R] = right[r];
                r++; // Перемещаем указатель правого массива
            }
            R++; // Перемещаем указатель результирующего массива
        }

        // === ДОЗАПИСЬ ОСТАВШИХСЯ ЭЛЕМЕНТОВ ===

        // Если в левом массиве остались элементы (правый уже закончился)
        while (l < left.length) {
            result[R] = left[l];
            l++;
            R++;
        }

        // Если в правом массиве остались элементы (левый уже закончился)
        while (r < right.length) {
            result[R] = right[r];
            r++;
            R++;
        }

        // Возвращаем отсортированный объединенный массив
        return result;
    }
}