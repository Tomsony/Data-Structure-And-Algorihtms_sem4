package sorts.ShellSort;

/**
 * Класс ShellSort реализует алгоритм сортировки Шелла (Shell Sort).
 *
 * Принцип работы:
 * 1. Алгоритм является усовершенствованной версией сортировки вставками
 * 2. Вместо сравнения соседних элементов, сравниваются элементы,
 *    находящиеся на определенном расстоянии (интервале/шаге) друг от друга
 * 3. Интервал постепенно уменьшается до 1, когда алгоритм становится
 *    обычной сортировкой вставками
 *
 * Преимущества алгоритма:
 * - Работает быстрее обычной сортировки вставками для больших массивов
 * - Не требует дополнительной памяти
 * - Прост в реализации
 *
 * Сложность алгоритма:
 * - Зависит от выбора последовательности интервалов (gap)
 * - В данной реализации (gap = n/2, gap/2, ...):
 *   * Худший случай: O(n²)
 *   * Средний случай: зависит от данных, обычно лучше чем O(n²)
 * - Пространственная сложность: O(1)
 *
 * Последовательность интервалов в данной реализации:
 *   n/2, n/4, n/8, ..., 1 (последовательность Шелла)
 *
 * @author Томских Артём ИВТ-23
 */
public class ShellSort {

    /**
     * Сортирует массив целых чисел методом Шелла.
     *
     * Алгоритм работы:
     * 1. Выбирается начальный интервал (gap = n/2)
     * 2. Пока интервал больше 0:
     *    a. Для каждого элемента, начиная с позиции gap:
     *       - Сохраняем текущий элемент в переменную temp
     *       - Сравниваем его с элементами на расстоянии gap слева
     *       - Сдвигаем большие элементы вправо на gap позиций
     *       - Вставляем temp на найденное место
     *    b. Уменьшаем интервал вдвое (gap /= 2)
     * 3. Возвращаем отсортированный массив
     *
     * Визуализация для массива [9, 8, 7, 6, 5, 4, 3, 2, 1]:
     *
     * Шаг 1: gap = 4 (n/2 = 4)
     *   Сравниваем и сортируем элементы на расстоянии 4:
     *   - [9, 5] → [5, 9]
     *   - [8, 4] → [4, 8]
     *   - [7, 3] → [3, 7]
     *   - [6, 2] → [2, 6]
     *   - [9, 1] → [1, 9]
     *   Результат: [5, 4, 3, 2, 1, 8, 7, 6, 9]
     *
     * Шаг 2: gap = 2
     *   Сортируем элементы на расстоянии 2:
     *   Результат: [1, 2, 3, 4, 5, 6, 7, 8, 9]
     *
     * Шаг 3: gap = 1 (обычная сортировка вставками)
     *   Финальная доводка массива
     *
     * @param arr входной массив целых чисел для сортировки
     * @return отсортированный массив (тот же объект, что и входной)
     *
     * @throws NullPointerException если массив равен null
     */
    public static int[] shellSort(int[] arr) {
        // Проверка на null
        if (arr == null) {
            throw new NullPointerException("Массив не может быть null");
        }

        int n = arr.length;

        // Если массив пуст или содержит один элемент, он уже отсортирован
        if (n <= 1) {
            return arr;
        }

        // === ВНЕШНИЙ ЦИКЛ: УПРАВЛЕНИЕ ИНТЕРВАЛАМИ ===
        // Начинаем с большого интервала (половина длины массива)
        // и последовательно уменьшаем его вдвое до 1
        for (int gap = n / 2; gap > 0; gap /= 2) {

            // === ВНУТРЕННИЙ ЦИКЛ: СОРТИРОВКА ВСТАВКАМИ С ТЕКУЩИМ ИНТЕРВАЛОМ ===
            // Проходим по элементам, начиная с позиции gap
            for (int i = gap; i < n; i++) {
                // Сохраняем текущий элемент во временную переменную
                int temp = arr[i];
                int j;

                // Сдвигаем элементы, стоящие на расстоянии gap слева,
                // если они больше temp
                for (j = i; j >= gap && arr[j - gap] > temp; j -= gap) {
                    arr[j] = arr[j - gap]; // Сдвиг элемента вправо на gap позиций
                }

                // Вставляем сохраненный элемент на найденное место
                arr[j] = temp;
            }
        }

        return arr;
    }


    /**
     * Вспомогательный метод для преобразования массива в строку.
     *
     * @param arr массив для преобразования
     * @return строковое представление массива
     */
    private static String arrayToString(int[] arr) {
        if (arr == null) return "null";
        if (arr.length == 0) return "[]";

        StringBuilder sb = new StringBuilder("[");
        for (int i = 0; i < arr.length; i++) {
            sb.append(arr[i]);
            if (i < arr.length - 1) {
                sb.append(", ");
            }
        }
        sb.append("]");
        return sb.toString();
    }
}