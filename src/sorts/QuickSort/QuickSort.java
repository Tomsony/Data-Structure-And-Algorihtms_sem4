package sorts.QuickSort;

/**
 * Класс QuickSort реализует алгоритм быстрой сортировки с оптимизированным
 * выбором опорного элемента по методу медианы из трёх.
 *
 * Принцип работы:
 * 1. Выбирается опорный элемент (pivot) - в данной реализации используется медиана из трёх
 * 2. Массив разделяется на две части: элементы меньше или равные pivot и элементы больше pivot
 * 3. Рекурсивно применяется тот же алгоритм к левой и правой частям
 *
 * Преимущества:
 * - Один из самых быстрых алгоритмов сортировки на практике
 * - Не требует дополнительной памяти, кроме стека вызовов
 * - Хорошая кэш-локальность
 *
 * Сложность алгоритма:
 * - Лучший случай: O(n log n) - когда pivot каждый раз делит массив пополам
 * - Средний случай: O(n log n) - для случайных данных
 * - Худший случай: O(n²) - когда массив уже отсортирован и выбран плохой pivot
 *   (в данной реализации с медианой из трёх худший случай встречается крайне редко)
 * - Пространственная сложность: O(log n) для стека рекурсии
 *
 * @author Томских Артём ИВТ-23
 */
public class QuickSort {

    /**
     * Основная функция быстрой сортировки.
     *
     * Алгоритм работы:
     * 1. Проверяет корректность входных параметров
     * 2. Если длина части массива больше 1 (low < high):
     *    a. Находит индекс опорного элемента с помощью partition()
     *    b. Рекурсивно сортирует левую часть (до pivot-1)
     *    c. Рекурсивно сортирует правую часть (от pivot+1)
     *
     * @param arr исходный массив для сортировки
     * @param low левая граница сортируемой части (включительно)
     * @param high правая граница сортируемой части (включительно)
     * @return отсортированный массив
     *
     * @throws ArrayIndexOutOfBoundsException если индексы выходят за пределы массива
     */
    public static int[] quickSort(int[] arr, int low, int high) {
        // Проверка корректности индексов
        if (low < 0 || high >= arr.length) {
            return arr; // Выход за границы массива - возвращаем без изменений
        }

        // Рекурсивно сортируем, пока длина части больше 1
        if (low < high) {
            // Получаем индекс опорного элемента после разделения
            int pivotIndex = partition(arr, low, high);

            // Рекурсивно сортируем левую часть (элементы меньше или равные pivot)
            quickSort(arr, low, pivotIndex - 1);

            // Рекурсивно сортируем правую часть (элементы больше pivot)
            quickSort(arr, pivotIndex + 1, high);
        }

        return arr;
    }

    /**
     * Функция разделения массива относительно опорного элемента.
     *
     * Оптимизация: выбор опорного элемента по методу "медиана из трёх"
     * (первый, средний и последний элементы). Это значительно уменьшает
     * вероятность худшего случая O(n²).
     *
     * Алгоритм работы:
     * 1. Находит медиану из трёх элементов (arr[low], arr[mid], arr[high])
     * 2. Перемещает опорный элемент в конец (индекс high)
     * 3. Проходит по всем элементам от low до high-1:
     *    - Если элемент <= pivot, меняет его местами с элементом на позиции i
     *    - Увеличивает i
     * 4. Возвращает опорный элемент на его правильную позицию (индекс i)
     * 5. Возвращает индекс опорного элемента
     *
     * Визуализация для массива [9, 7, 5, 11, 12, 2, 14, 3, 10, 6]:
     *
     * Шаг 1: Выбор медианы из трёх (low=0, mid=4, high=9)
     *   arr[0]=9, arr[4]=12, arr[9]=6
     *   После сравнений: 6, 9, 12 -> медиана = 9
     *
     * Шаг 2: Перемещаем pivot (9) в конец
     *   [6, 7, 5, 11, 12, 2, 14, 3, 10, 9]
     *
     * Шаг 3: Разделение (i = low = 0)
     *   j=0: arr[0]=6 <=9? Да -> swap(0,0), i=1
     *   j=1: arr[1]=7 <=9? Да -> swap(1,1), i=2
     *   j=2: arr[2]=5 <=9? Да -> swap(2,2), i=3
     *   j=3: arr[3]=11 <=9? Нет
     *   j=4: arr[4]=12 <=9? Нет
     *   j=5: arr[5]=2 <=9? Да -> swap(3,5): [6,7,5,2,12,11,14,3,10,9], i=4
     *   j=6: arr[6]=14 <=9? Нет
     *   j=7: arr[7]=3 <=9? Да -> swap(4,7): [6,7,5,2,3,11,14,12,10,9], i=5
     *   j=8: arr[8]=10 <=9? Нет
     *
     * Шаг 4: Возвращаем pivot на место
     *   swap(5,9): [6,7,5,2,3,9,14,12,10,11]
     *
     * Результат: все элементы слева от 9 <=9, справа >9
     *
     * @param arr массив для разделения
     * @param low левая граница разделяемой части
     * @param high правая граница разделяемой части
     * @return индекс опорного элемента после разделения
     */
    public static int partition(int[] arr, int low, int high) {
        // === ОПТИМИЗАЦИЯ: Медиана из трёх для выбора опорного элемента ===
        int mid = low + (high - low) / 2;

        // Сортируем три элемента, чтобы средний стал медианой
        if (arr[low] > arr[mid]) {
            swap(arr, low, mid); // low <= mid
        }
        if (arr[low] > arr[high]) {
            swap(arr, low, high); // low <= high
        }
        if (arr[mid] > arr[high]) {
            swap(arr, mid, high); // mid <= high
        }

        // Теперь медиана находится в mid, а наибольший элемент - в high
        // Перемещаем опорный элемент (медиану) в конец для удобства
        swap(arr, mid, high);
        int pivot = arr[high]; // Опорный элемент теперь в конце

        // === ОСНОВНОЕ РАЗДЕЛЕНИЕ ===
        int i = low; // i - граница для элементов <= pivot

        // Проходим по всем элементам, кроме последнего (там pivot)
        for (int j = low; j < high; j++) {
            // Если текущий элемент меньше или равен опорному
            if (arr[j] <= pivot) {
                // Меняем его с элементом на позиции i
                swap(arr, i, j);
                i++; // Увеличиваем границу для элементов <= pivot
            }
            // Если элемент > pivot, просто пропускаем его
        }

        // Возвращаем опорный элемент на его правильную позицию
        // Теперь все элементы слева от i <= pivot, справа > pivot
        swap(arr, i, high);

        // Возвращаем индекс опорного элемента
        return i;
    }

    /**
     * Вспомогательный метод для обмена двух элементов в массиве.
     *
     * @param arr массив, в котором производится обмен
     * @param i индекс первого элемента
     * @param j индекс второго элемента
     */
    private static void swap(int[] arr, int i, int j) {
        int temp = arr[i];
        arr[i] = arr[j];
        arr[j] = temp;
    }

    /**
     * Упрощенный метод для сортировки всего массива.
     *
     * @param arr массив для сортировки
     * @return отсортированный массив
     */
    public static int[] quickSort(int[] arr) {
        if (arr == null || arr.length <= 1) {
            return arr;
        }
        return quickSort(arr, 0, arr.length - 1);
    }

    /**
     * Вспомогательный метод для вывода массива на экран.
     *
     * @param arr массив для вывода
     * @return строковое представление массива
     */
    private static String arrayToString(int[] arr) {
        if (arr == null) return "null";
        if (arr.length == 0) return "[]";

        StringBuilder sb = new StringBuilder("[");
        for (int i = 0; i < arr.length; i++) {
            sb.append(arr[i]);
            if (i < arr.length - 1) {
                sb.append(", ");
            }
        }
        sb.append("]");
        return sb.toString();
    }
}