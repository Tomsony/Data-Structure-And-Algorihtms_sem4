package stack;

import java.util.EmptyStackException;

/**
 * Класс Stack реализует структуру данных "стек" с принципом LIFO (последний вошел, первый вышел).
 *
 * Стек построен на основе односвязного списка, что обеспечивает:
 * - Добавление элемента O(1)
 * - Удаление элемента O(1)
 * - Получение верхнего элемента O(1)
 *
 * Временная сложность операций:
 * - push: O(1) - добавление в начало списка
 * - pop: O(1) - удаление из начала списка
 * - peek: O(1) - просмотр первого элемента
 * - search: O(n) - линейный поиск по стеку
 * - size: O(1) - возврат сохраненного значения
 *
 * Пространственная сложность: O(n), где n - количество элементов
 *
 * @param <T> тип элементов стека
 * @author Томских Артём ИВТ-23
 */
public class Stack<T> implements StackInterface<T>  {

    /**
     * Внутренний класс для представления узла односвязного списка.
     * Содержит данные и ссылку на следующий узел.
     */
    private static class Node<T> {
        T data;         // Данные, хранящиеся в узле
        Node<T> next;   // Ссылка на следующий узел в стеке

        /**
         * Создает новый узел с указанными данными.
         *
         * @param data данные для хранения в узле
         * @throws NullPointerException если data равен null
         */
        Node(T data) {
            if (data == null) {
                throw new NullPointerException("Стек не может содержать null элементы");
            }
            this.data = data;
            this.next = null;
        }
    }

    private Node<T> top;    // Вершина стека (последний добавленный элемент)
    private int size;       // Текущее количество элементов в стеке

    /**
     * Создает пустой стек.
     * Инициализирует вершину стека как null и размер как 0.
     */
    public Stack() {
        top = null;
        size = 0;
    }

    /**
     * Добавляет элемент на вершину стека.
     *
     * Алгоритм:
     * 1. Создает новый узел с переданным элементом
     * 2. Новый узел указывает на текущую вершину
     * 3. Вершина стека становится новым узлом
     * 4. Увеличивается размер стека
     *
     * Временная сложность: O(1)
     *
     * @param element элемент для добавления
     * @throws NullPointerException если element равен null
     */
    public void push(T element) {
        // Проверка на null
        if (element == null) {
            throw new NullPointerException("Нельзя добавить null в стек");
        }

        Node<T> newNode = new Node<>(element); // Создаем новый узел
        newNode.next = top;                    // Новый узел ссылается на текущую вершину
        top = newNode;                         // Обновляем вершину
        size++;                                // Увеличиваем размер
    }

    /**
     * Удаляет и возвращает элемент с вершины стека.
     *
     * Алгоритм:
     * 1. Проверяет, не пуст ли стек
     * 2. Сохраняет данные с вершины
     * 3. Перемещает вершину на следующий элемент
     * 4. Уменьшает размер стека
     * 5. Возвращает сохраненные данные
     *
     * Временная сложность: O(1)
     *
     * @return элемент с вершины стека
     * @throws EmptyStackException если стек пуст
     */
    public T pop() {
        if (isEmpty()) {
            throw new EmptyStackException();
        }
        T data = top.data;      // Сохраняем данные с вершины
        top = top.next;         // Перемещаем вершину на следующий элемент
        size--;                 // Уменьшаем размер
        return data;            // Возвращаем удаленный элемент
    }

    /**
     * Возвращает элемент с вершины стека без его удаления.
     *
     * Алгоритм:
     * 1. Проверяет, не пуст ли стек
     * 2. Возвращает данные с вершины
     *
     * Временная сложность: O(1)
     *
     * @return элемент с вершины стека
     * @throws EmptyStackException если стек пуст
     */
    public T peek() {
        if (isEmpty()) {
            throw new EmptyStackException();
        }
        return top.data;
    }

    /**
     * Ищет элемент в стеке и возвращает его позицию.
     *
     * Позиция считается от вершины стека:
     * - 1 - элемент на вершине
     * - 2 - следующий элемент
     * - и так далее
     * - -1 - элемент не найден
     *
     * Алгоритм:
     * 1. Начинает с вершины стека
     * 2. Сравнивает каждый элемент с искомым
     * 3. Если элемент найден, возвращает его позицию
     * 4. Если достигнут конец стека, возвращает -1
     *
     * Временная сложность: O(n) в худшем случае
     *
     * @param element элемент для поиска
     * @return позиция элемента (1-индексация) или -1, если элемент не найден
     */
    public int search(T element) {
        Node<T> current = top;
        int position = 1;

        while (current != null) {
            // Используем equals для сравнения (null уже отсекли в push)
            if (current.data.equals(element)) {
                return position;
            }
            current = current.next;
            position++;
        }

        return -1; // Элемент не найден
    }

    /**
     * Проверяет, пуст ли стек.
     *
     * Временная сложность: O(1)
     *
     * @return true если стек пуст, false в противном случае
     */
    public boolean isEmpty() {
        return top == null;
    }

    /**
     * Возвращает количество элементов в стеке.
     *
     * Временная сложность: O(1)
     *
     * @return размер стека
     */
    public int size() {
        return size;
    }

    /**
     * Очищает стек, удаляя все элементы.
     *
     * Алгоритм:
     * 1. Устанавливает вершину в null
     * 2. Сбрасывает размер в 0
     *
     * Сборщик мусора автоматически удалит неиспользуемые узлы
     *
     * Временная сложность: O(1)
     */
    public void clear() {
        top = null;  // Все узлы становятся недостижимыми
        size = 0;    // Сбрасываем размер
    }

    /**
     * Возвращает строковое представление стека.
     * Элементы выводятся от вершины к основанию.
     *
     * Временная сложность: O(n)
     *
     * @return строка с элементами стека в формате [элемент1, элемент2, ...]
     */
    @Override
    public String toString() {
        StringBuilder sb = new StringBuilder("[");
        Node<T> current = top;

        while (current != null) {
            sb.append(current.data);
            if (current.next != null) {
                sb.append(", ");
            }
            current = current.next;
        }

        sb.append("]");
        return sb.toString();
    }
}